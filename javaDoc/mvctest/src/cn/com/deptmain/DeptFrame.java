	//增添部门信息
	private void add() {
		//new一个部门对象
		Dept dept = new Dept();
		String deptName = this.deptName.getText();
		String deptDesc = this.deptDesc.getText();
		if (StringUtil.isNullOrEmpty(deptName)
				&& !StringUtil.isNullOrEmpty(deptDesc)) {
			JOptionPane.showMessageDialog(null, "请输入要添加的部门名");
		}
		if (StringUtil.isNullOrEmpty(deptDesc)
				&& !StringUtil.isNullOrEmpty(deptName)) {
			JOptionPane.showMessageDialog(null, "请输入要添加的部门描述");
		}
		if (StringUtil.isNullOrEmpty(deptName)
				&& StringUtil.isNullOrEmpty(deptDesc)) {
			JOptionPane.showMessageDialog(null, "请输入要添加的部门名和描述");
		}

		if (!StringUtil.isNullOrEmpty(deptName)
				&& !StringUtil.isNullOrEmpty(deptDesc)) {
			dept.setDeptName(deptName);
			dept.setDeptDesc(deptDesc);
			int count = dao.AddDeptInfIntoOracle(dept);
			if (count == 1) {
				JOptionPane.showMessageDialog(null, "部门信息添加成功");
				this.deptName.setText(null);
				this.deptDesc.setText(null);
			} else {
				JOptionPane.showMessageDialog(null, "部门信息添加失败");
			}
		}

	}

